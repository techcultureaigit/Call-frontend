import type { Campaign, CampaignStats } from "@/types/campaign";

function daysAgo(days: number): string {
  return new Date(Date.now() - days * 86400000).toISOString();
}

function hoursAgo(hours: number): string {
  return new Date(Date.now() - hours * 3600000).toISOString();
}

function makeStats(
  total: number,
  completed: number,
  failed: number,
  responses: number,
  successRate: number
): CampaignStats {
  return {
    totalCalls: total,
    completedCalls: completed,
    failedCalls: failed,
    pendingCalls: total - completed - failed,
    successRate,
    responses,
    avgDurationSeconds: 240 + Math.floor(Math.random() * 120),
    lastUpdatedAt: hoursAgo(0),
  };
}

