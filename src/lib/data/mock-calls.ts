import type { Call, CallTimelineEvent } from "@/types/call";

function minutesAgo(mins: number): string {
  return new Date(Date.now() - mins * 60000).toISOString();
}

function hoursAgo(hours: number): string {
  return new Date(Date.now() - hours * 3600000).toISOString();
}

function daysAgo(days: number): string {
  return new Date(Date.now() - days * 86400000).toISOString();
}

function buildTimeline(
  startedAt: string,
  status: Call["status"],
  duration: number
): CallTimelineEvent[] {
  const start = new Date(startedAt).getTime();
  const events: CallTimelineEvent[] = [
    {
      id: `tl_${Date.now()}_1`,
      type: "initiated",
      label: "Call initiated",
      description: "Outbound dial started",
      occurredAt: startedAt,
    },
    {
      id: `tl_${Date.now()}_2`,
      type: "ringing",
      label: "Ringing",
      occurredAt: new Date(start + 5000).toISOString(),
    },
  ];

  if (["completed", "in_progress"].includes(status)) {
    events.push({
      id: `tl_${Date.now()}_3`,
      type: "answered",
      label: "Call answered",
      occurredAt: new Date(start + 12000).toISOString(),
    });
    events.push({
      id: `tl_${Date.now()}_4`,
      type: "recording_started",
      label: "Recording started",
      occurredAt: new Date(start + 14000).toISOString(),
    });
  }

  if (status === "completed") {
    events.push({
      id: `tl_${Date.now()}_5`,
      type: "completed",
      label: "Call completed",
      description: `Duration: ${Math.floor(duration / 60)}m ${duration % 60}s`,
      occurredAt: new Date(start + duration * 1000).toISOString(),
    });
  }

  if (["failed", "no_answer", "busy"].includes(status)) {
    events.push({
      id: `tl_${Date.now()}_6`,
      type: "failed",
      label: status === "no_answer" ? "No answer" : status === "busy" ? "Line busy" : "Call failed",
      occurredAt: new Date(start + 30000).toISOString(),
    });
  }

  return events;
}

